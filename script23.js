//Если а и b отрицательные, вывести их произведение;
//Если а и b разных знаков, вывести их сумму;
var a=+prompt("Введите a:");
var b=+prompt("Введите b:");
if(a>=0 && b>=0)
{
	alert(a-b);
}
else if(a<0 && b<0)
{
	alert(a*b);
}
else
{
	alert(a+b);
}