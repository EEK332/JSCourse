//Присвоить переменной а значение в промежутке [0..15]. Вывести числа от a до 15
//Реализация через рекурсию
function rec(a,str="")
{
	if(a==15)
	{
		return (str+a);
	}
	str+=a++ +" ";
	return rec(a,str);
}

var a = prompt("Ведите число от 0 до 15");
if (a>=0 && a<=15)
{
	alert(rec(a));
}
else
{
	alert("Введено неверное значение!");
}